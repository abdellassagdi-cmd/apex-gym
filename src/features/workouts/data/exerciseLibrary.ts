function createExercise(seed: ExerciseSeed): Exercise {
  const eligibleLevels: Exercise["eligibleLevels"] = seed.eligibleLevels ??
    (seed.intensity === "strength"
      ? ["intermediate", "advanced", "athlete"]
      : seed.intensity === "conditioning"
        ? ["beginner", "intermediate", "advanced", "athlete"]
        : ["beginner", "intermediate", "advanced"]);
  const exercise: Exercise = {
    coachCue:
      seed.coachCue ??
      "Stay controlled, keep the target muscle loaded, and stop the set before technique breaks.",
    equipment: seed.equipment ?? "Cable",
    gifUrl: seed.gifUrl ?? motionLibrary[seed.targetMuscle],
    instructions: seed.instructions ?? defaultInstructions[seed.targetMuscle],
    eligibleLevels,
    ...seed,
    ...verifiedMotionById[seed.id],
  };
  return {
    ...exercise,
    secondaryMuscles: [...new Set(exercise.secondaryMuscles)].filter((muscle) => muscle !== exercise.targetMuscle),
  };
}

